// ── Helpers ───────────────────────────────────────────────────────────────────

/** PII guard (inlined to avoid circular dep with KbRagCopilotService). */
const PLAN_PII_PATTERNS: Array<[RegExp, string]> = [
  [/(?:\+55\s*)?(?:\(?\d{2}\)?\s*)?\d{4,5}[\s.-]?\d{4}\b/g, ''],
  [/\b[\w.+-]+@[\w-]+\.[\w.]+\b/gi, ''],
  [/\b\d{3}\.?\d{3}\.?\d{3}-?\d{2}\b/g, ''],
  [/\b\d{2}\.?\d{3}\.?\d{3}[/\\]?\d{4}-?\d{2}\b/g, ''],
  [/(?:token|senha|password|secret|api[_-]?key|bearer)\s*[:=]?\s*["']?\S{8,}["']?/gi, ''],
];

function guardPii(text: string): string {
  let result = text;
  for (const [pattern, replacement] of PLAN_PII_PATTERNS) {
    result = result.replace(pattern, replacement);
  }
  return result;
}

/** Normalize for anchor matching: PII-free, lowercase, de-accented, whitespace-collapsed. */
export function normalizeForPlan(text: string): string {
  return guardPii(String(text ?? ''))
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .replace(/[^\w\s]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .slice(0, 400);
}

function detectAnchor(normalizedQuery: string): ProductAnchor | null {
  for (const anchor of PRODUCT_ANCHORS) {
    for (const alias of anchor.aliases) {
      const normalizedAlias = alias
        .toLowerCase()
        .normalize('NFD')
        .replace(/[̀-ͯ]/g, '');
      if (normalizedQuery.includes(normalizedAlias)) {
        return anchor;
      }
    }
  }
  return null;
}

function detectIntent(query: string): Intent {
  // Run on de-accented lowercase for consistent matching
  const normalized = query
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '');

  for (const { intent, patterns } of INTENT_PATTERNS) {
    if (patterns.some((p) => p.test(normalized))) {
      return intent;
    }
  }
  return 'generic';
}

/** Strip forbidden fields from a plan object (runtime safety gate). */
function validatePlan(plan: SearchPlan): SearchPlan {
  const obj = plan as unknown as Record<string, unknown>;
  for (const field of FORBIDDEN_PLAN_FIELDS) {
    if (Object.prototype.hasOwnProperty.call(obj, field)) {
      // eslint-disable-next-line @typescript-eslint/no-dynamic-delete
      delete obj[field];
    }
  }
  return plan;
}

// ── Service ───────────────────────────────────────────────────────────────────

export interface KbPlanClientContext {
  productOrSystem?: string | null;
  category?: string | null;
}

export class KbSearchPlannerService {
  /**
   * @param _ollamaPort Reserved for future local AI enrichment — pass null for
   *                    deterministic-only mode. Cloud AI is structurally absent.
   */
  public constructor(
    // eslint-disable-next-line @typescript-eslint/no-unused-vars
    private readonly _ollamaPort: { generateText(prompt: string): Promise<string> } | null = null,
  ) {}

  /**
   * Build a SearchPlan for the given query.
   *
   * @param query        Raw query (PII guard applied internally).
   * @param clientContext Optional client context (product hint).
   */
  public async buildPlan(
    query: string,
    clientContext?: KbPlanClientContext | null,
  ): Promise<SearchPlan> {
    const normalizedQuery = normalizeForPlan(String(query ?? '').trim());
    const anchor = detectAnchor(normalizedQuery);
    const intent = detectIntent(query);

    let plan: SearchPlan;

    if (anchor !== null) {
      plan = this.buildAnchoredPlan(normalizedQuery, anchor, intent);
    } else if (clientContext?.productOrSystem) {
      // Try to resolve product hint to an anchor
      const ctxNorm = normalizeForPlan(clientContext.productOrSystem);
      const ctxAnchor = PRODUCT_ANCHORS.find((a) =>
        a.aliases.some((al) => {
          const normalizedAlias = al.toLowerCase().normalize('NFD').replace(/[̀-ͯ]/g, '');
          return ctxNorm.includes(normalizedAlias) || normalizedAlias.includes(ctxNorm);
        }),
      ) ?? null;

      plan = ctxAnchor !== null
        ? this.buildAnchoredPlan(normalizedQuery, ctxAnchor, intent)
        : this.buildGenericPlan(normalizedQuery, intent, clientContext);
    } else {
      plan = this.buildGenericPlan(normalizedQuery, intent, clientContext);
    }

    return validatePlan(plan);
  }

  private buildAnchoredPlan(
    normalizedQuery: string,
    anchor: ProductAnchor,
    intent: Intent,
  ): SearchPlan {
    return {
      normalizedQuery,
      intent,
      productOrSystem: anchor.name,
      domain: anchor.domain,
      symptoms: [],
      mustTerms: [...anchor.mustTerms],
      boostTerms: [...anchor.boostTerms],
      negativeTerms: [...anchor.negativeTerms],
      negativeDomains: [...anchor.negativeDomains],
      sourceTiersAllowed: [anchor.sourceTier, 'tier_2_operational_kb'],
      minimumConfidence: anchor.minimumConfidence,
      topK: anchor.topK,
      reason: `Product anchor: ${anchor.name} (domain: ${anchor.domain})`,
      planSource: 'deterministic',
    };
  }

  private buildGenericPlan(
    normalizedQuery: string,
    intent: Intent,
    clientContext?: KbPlanClientContext | null,
  ): SearchPlan {
    const tokenCount = normalizedQuery
      .split(/\s+/)
      .filter((t) => t.length >= 3).length;
    const isVeryGeneric = tokenCount <= 2;

    return {
      normalizedQuery,
      intent,
      productOrSystem: clientContext?.productOrSystem ?? null,
      domain: null,
      symptoms: [],
      mustTerms: [],
      boostTerms: [],
      negativeTerms: [],
      negativeDomains: [],
      sourceTiersAllowed: [
        'tier_1_product_specific',
        'tier_2_operational_kb',
        'tier_3_generic_playbook',
      ],
      minimumConfidence: isVeryGeneric ? 0.80 : 0.70,
      topK: 5,
      reason: isVeryGeneric
        ? 'Very generic query — elevated confidence threshold'
        : 'No product anchor detected — generic search',
      planSource: 'deterministic',
    };
  }
}
