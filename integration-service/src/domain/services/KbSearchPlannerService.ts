/**
 * KbSearchPlannerService — Deterministic search plan generator for KB RAG.
 *
 * Generates a controlled SearchPlan before every KB search, replacing ad-hoc
 * filter accumulation with a structured, auditable plan. The plan specifies:
 *   - normalizedQuery (PII-free, de-accented)
 *   - intent (semantic category)
 *   - productOrSystem (from product anchors)
 *   - mustTerms, boostTerms, negativeTerms, negativeDomains
 *   - sourceTiersAllowed
 *   - minimumConfidence (threshold — KB_INSUFFICIENT if not met after ranking)
 *
 * Flow:
 *   1. Normalize and PII-guard the query.
 *   2. Detect product/system via product anchors (deterministic).
 *   3. Detect intent via pattern matching.
 *   4. Build SearchPlan from anchor or generic template.
 *   5. Validate: remove forbidden fields (raw_sql, sql_query, executable_command).
 *   6. Optional: local AI enrichment (placeholder — deterministic only for phase 1).
 *
 * Source tiers:
 *   tier_1_product_specific (0.60) — articles for a named product anchor
 *   tier_2_operational_kb   (0.70) — operational/generic KB
 *   tier_3_generic_playbook (0.85) — catch-all generic playbooks
 *   tier_4_automation       (0.95) — automation scripts (rarely searched)
 *
 * Invariants:
 *   - Deterministic first. Local AI is optional enrichment.
 *   - NO cloud AI (structurally absent).
 *   - NO SQL generated by AI.
 *   - NO command execution.
 *   - NO MariaDB access.
 *   - PII-free normalizedQuery at all times.
 *
 * Phase: integaglpi_kb_rag_ai_search_planner_hybrid_retrieval_001
 */

// ── Types ─────────────────────────────────────────────────────────────────────

export type Intent =
  | 'application_not_opening'
  | 'identity_sync'
  | 'backup_restore'
  | 'server_slow'
  | 'network_issue'
  | 'license_activation'
  | 'generic';

export type SourceTier =
  | 'tier_1_product_specific'
  | 'tier_2_operational_kb'
  | 'tier_3_generic_playbook'
  | 'tier_4_automation';

